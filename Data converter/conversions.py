# Байты в Килобайты. 1.
def bytes_to_kilobytes(bytes):
    return bytes / 1024

# Килобайты в Мегабайты. 2.
def kilobytes_to_megabytes(kilobytes):
    return kilobytes / 1024

# Мегабайты в Гигабайты. 3.
def megabytes_to_gigabytes(megabytes):
    return megabytes / 1024

# Гигабайты в Терабайты. 4.
def gigabytes_to_terabytes(gigabytes):
    return gigabytes / 1024

# Килобайты в Байты. 5.
def kilobytes_to_bytes(kilobytes):
    return kilobytes * 1024

