# Задача: напишите программу, которая ищет слова, символы или числа внутри предложения.
# Реализуйте функционал с помощью вложенного цикла while(True), или for.

# Запускаем бесконечный цикл while.
while True:
    try:

        # Приглашение к вводу строки.
        # Обычный input обеспечивает нам ввод любых знаков - букв, цифр, дробей, чего угодно...
        value = input('Введите слова, символы, или предложения:~$ ')

        # Приглашение к поиску фрагмента внутри строки.
        search_symbols = input('Введите слово, число или символ для поиска:~$ ')

        # Инициализация счётчика.
        count = 0

        # Проверка, является ли искомое значение числом.
        # Это поможет искать целые числа внутри строки.
        if search_symbols.isdigit():

            # Разделяем строку на слова по пробелам.
            words = value.split()

           # Подсчитываем сколько раз искомое число встречается как отдельное слово.
            count = words.count(search_symbols)

        # Иначе ищем вхождения строки внутри строки.
        else:

            # Переменная start = 0. Это индекс, с которого начнётся поиск.
            start = 0

            # Вложенный while(True) ищет вхождения search_symbols в строке value
            # начиная с позиции start.
            while(True):

                # Метод value.find(search_symbols, start) возвращает индекс первого
                # вхождения искомого фрагмента, начиная с позиции start, или -1, если
                # вхождения больше нет.
                index = value.find(search_symbols, start)

                # Если index равен -1, цикл прерывается, так как все вхождения найдены.
                if index == -1:
                    break

                # Если вхождение найдено, счётчик count увеличивается на 1.
                count += 1

                # Переменная start обновляется на index + 1, чтобы искать дальше,
                # начиная со следующего символа после найденного вхождения.
                # Это позволяет находить все вхождения, даже если они перекрываются
                # или идут подряд.
                start = index + 1  # Ищем дальше после текущего вхождения.

        # Принтуем результат на экран.
        print('-'*35)
        print('Количество найденных совпадений:', count)
        print('-'*35)

    except:
        continue