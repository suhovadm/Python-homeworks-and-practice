# Определяем функцию zapolnenie_ozenok, которая принимает в себя 1 аргумент - spisok.
def zapolnenie_ozenok(spisok):

    # Заводим цикл for (от 0 до 9), который просит пользователя ввести оценки для каждого из 10 экзаменов.
    for i in range(10):

        # Заводим бесконечный цикл while.
        while(True):

            # Приглашение к вводу команды.
            # Через {i+1} вставляется текущий номер экзамена от 1 до 10.
            # int() - преобразует введённую строку в целое число и присваивает его переменной ozenka.
            ozenka = int(input(f'Введите оценку {i+1} экзамена (от 0 до 100):~$ '))

            # Создаём условие.
            # Проверяем, входит ли значение ozenka в допустимый диапазон от 0 до 100 включительно.
            # Используется цепочка сравнений: сначала 0 <= ozenka, затем ozenka <= 100.
            # Если оба условия истинны, то блок внутри if выполняется.
            if 0 <= ozenka <= 100:

                # Записываем введённую оценку ozenka в список spisok по индексу i.
                # Это означает, что для текущего экзамена (номер i+1) сохраняется оценка.
                spisok[i] = ozenka

                # Выход из бесконечного цикла while, потому что оценка корректна и записана.
                # После этого цикл for переходит к следующему экзамену.
                break

            # Если условие if не выполнено (то есть оценка вне диапазона 0 - 100), то срабатывает блок else
            # и выводится сообщение об ошибке: некорректный ввод. Пожалуйста, введите число от 0 до 100.
            else:
                print('\nНекорректный ввод. Пожалуйста, введите число от 0 до 100.\n')