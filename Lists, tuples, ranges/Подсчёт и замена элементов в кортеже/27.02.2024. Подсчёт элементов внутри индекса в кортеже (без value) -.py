# Задача: есть кортеж с целыми числами.
# Нужно вывести статистику по количеству цифр в элементах кортежа.
# Например:
# Одна цифра — 3 элемента;
# Две цифры — 4 элемента;
# Три цифры — 5 элементов.

# * В данном случае, задача решена не совсем верно, так как индекс элемента указан вручную.

numbers = ('123', '1234', '12345') # Сам кортеж.

# Заводим рандомную переменную, перекидываем кортеж в список, получаем доступ к индексу списка.
# В нашем случае, второй (последний) индекс.
a = list(numbers[2])

# Выводим кортеж, как он был.
print('-'*36)
print('Сам кортеж:',numbers)
print('-'*36)

# Это просто надпись.
print('Статистика по кортежу.')

# Считаем len-ом количество индексов.
print('Количество индексов в кортеже:', len(numbers))
# Тем же len-ом считаем количество элементов внутри индекса.
print('Количество элементов в индексе:', len(a))
print('-'*36)
