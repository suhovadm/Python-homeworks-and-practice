# Задача: напишите обычную итеративную функцию,
# которая проверяет число на палиндром.
# Вариант #2.

# !!!
# Определение функции внутри цикла НЕ является хорошей практикой.
# Преобразование ввода во float, а затем сразу в список - НЕ сработает,
# так как float НЕ является итерируемым объектом, и его нельзя преобразовать в список.
# Для проверки палиндрома лучше работать со строкой, а не с числом типа float.

# Итак!
# В данном случае, функция is_palindrome вынесена за пределы цикла.
# Ввод пользователя сохраняется как строка user_input.
# Проверка, что ввод - это число, делается через float(user_input).
# Для проверки палиндрома сравниваем строку с её обратной.
# Обработка ошибок - при вводе некорректных данных выводится сообщение и цикл продолжается.
# Если нужен более строгий контроль ввода, например, только целые числа, можно дополнительно
# проверять user_input.isdigit() или использовать регулярные выражения.

def is_palindrome(number_str):
    return number_str == number_str[::-1] # Сравниваем строку с обраткой.

while(True):
    try:
        user_input = input('Введите число для проверки на палиндром:~$ ')
        float(user_input)

        if is_palindrome(user_input):
            print('Палиндром!')
        else:
            print('Не палиндром!')

    except ValueError:
        print('Ошибка! Можно вводить только числовые значения.')
        continue