# Задача: пользователь вводит часы и минуты, прошедшие с начала дня.
# Программа, на выбор, должна показать, сколько секунд, минут, или часов осталось до конца дня.
# При выполнении задачи нужно использовать операторы ветвлений if / elif.
# ---------------------------------------------------------------------------------------------

# Заводим переменные.
# Количество секунд в сутках. 24 часа * 3600 секунд в часе = 86400 секунд.
SECONDS_IN_DAY = 86400
# Количество минут в сутках. 24 часа * 60 минут в часе = 1440 минут.
MINUTES_IN_DAY = 1440
# Количество часов в сутках. 24 - это количество часов в сутках.
HOURS_IN_DAY = 24

# Заводим бесконечный цикл while.
while(True):
    try:

        # Заголовок программы.
        print('[ Программа, вычисляющая количество времени, оставшееся до полуночи ]')
        print()

        # Приглашение на ввод часов.
        hours= int(input('> Введите текущий час (от 0 до 23):~$ '))

        # Приглашение на ввод минут.
        minutes = int(input('> Введите текущие минуты (от 0 до 59):~$ '))

        # Проверка, чтобы введённые часы находились строго в диапазоне от 0 до 23 включительно.
        if hours < 0 or hours >= HOURS_IN_DAY:
            print('Ошибка: часы должны быть в диапазоне от 0 до 23.')
            continue

        # Проверка, чтобы введённые минуты находились строго в диапазоне от 0 до 60 включительно.
        if minutes < 0 or minutes >= 60:
            print('Ошибка: минуты должны быть в диапазоне от 0 до 59.')
            continue

        # Вычисляем общее количество минут, прошедших с начала дня. Для этого умножается количество часов на 60
        # и прибавляется количество минут.
        total_minutes_passed = hours * 60 + minutes

        # Переводим прошедшее время в секунды, умножая минуты на 60.
        total_seconds_passed = total_minutes_passed * 60

        # Вычисляем сколько секунд осталось до конца дня: от общего количества секунд в сутках отнимаем уже прошедшие.
        remaining_seconds = SECONDS_IN_DAY - total_seconds_passed

        # Далее целочисленным (//) делим оставшиеся секунды на 3600, чтобы получить количество целых часов,
        # оставшихся до полуночи.
        # Затем берём остаток от деления этих секунд на 3600 (%), чтобы получить секунды, оставшиеся после
        # учёта полных часов, и делим их на 60, чтобы получить оставшиеся полные минуты.
        remaining_hours = remaining_seconds // 3600
        remaining_minutes = (remaining_seconds % 3600) // 60

        # Главное меню программы.
        print('-' * 35)
        print('[ До конца дня осталось ? ]')
        print('> 1. Количество секунд.')
        print('> 2. Количество минут.')
        print('> 3. Время в часах и минутах.')
        print('-' * 35)

        # Выбор главного меню программы.
        choice = int(input('> Что конкретно хотим узнать, 1-е, 2-е, или 3-е ? '))

        # Обработка выбора через if / elif. Кнопки главного меню, короч.

        # Выводит кол-во оставшихся секунд.
        # Число приводится к целому с помощью int(), чтобы убрать дробные части.
        if choice == 1:
            print('> До полуночи осталось:', int(remaining_seconds), 'сек.')
            print('-' * 35)

        # Переводим оставшееся время в минуты.
        # Время в секундах: remaining_seconds - это количество секунд, оставшихся до полуночи.
        # Чтобы узнать, сколько минут остается, нужно понять, сколько полных минут содержится в этом числе секунд.
        # В 1 минуте 60 секунд, поэтому это деление переводит секунды в минуты.
        # Почему делим, а не умножаем?
        # Потому что у нас есть количество секунд, а мы хотим получить количество минут - то есть, сколько полных
        # минут в этом числе секунд. Деление на 60 даёт именно это.
        # Дальше мы округляем получившееся значение до ближайшего целого числа с помощью round().
        elif choice == 2:
            remaining_minutes_total = remaining_seconds / 60
            print('> До полуночи осталось:', round(remaining_minutes_total), 'мин.')
            print('-' * 35)

        # Значения remaining_hours и remaining_minutes приводятся к целым числам с помощью int(), чтобы убрать
        # дробную часть и правильно отобразить целое число часов и минут.
        elif choice == 3:
            print(f'> До полуночи осталось: {int(remaining_hours)} час(ов) и {int(remaining_minutes)} мин.')
            print('-' * 35)

        # Перехват ошибок на тот случай, если пользователь выбрал что-то отличное от 1, 2 или 3.
        else:
            print('Ошибка! Некорректный выбор. Пожалуйста, выберите 1, 2 или 3.')

    # Перехват ошибок на тот случай, если пользователь ввёл не число, а букву, или спец. символ.
    except ValueError:
        print('Ошибка! Пожалуйста, введите целое числовое значение для часов и минут.')
        continue