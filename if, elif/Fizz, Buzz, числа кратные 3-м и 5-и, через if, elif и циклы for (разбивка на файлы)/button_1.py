# Кнопка 1. Fizz / числа кратные 3-м.
def handle_fizz(value1, value2):

    # Показывает диапазон между числами.
    for i in range(value1, value2 + 1):

        # Показывает числа кратные 3.
        if i % 3 == 0:

            # Если числа кратные 3, подгоняет к ним надпись ' Fizz '.
            print(i, '/ Fizz.')