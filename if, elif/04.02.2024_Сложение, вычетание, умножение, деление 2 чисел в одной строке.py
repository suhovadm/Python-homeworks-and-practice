# Задача: напишите программу, которая с помощью операторов ветвлений if / elif
# способна производить математические действия с двумя числами. Сложение, вычетание,
# умножение, деление. У программы должно быть меню и возможность выхода.
# Сделайте обработку ошибок у 4 пункта, чтобы нельзя было делить на ноль.

# Запускаем цикл.
while(True):
    try:

            # Меню.
            print('1. Сложение.') # value 1.
            print('2. Вычетание.') # value 2.
            print('3. Умножение.') # value 3.
            print('4. Деление.') # value 4.
            print('5. Выход.') # value 5.

            # Выбор пункта меню выше.
            value = int(input('Выберите арифметическое действие (от 1 до 4):~$ '))

            # Кнопка 1. Сумма двух чисел.
            if (value == 1):

                # Приглашение к вводу команды, в нашем случае, целых чисел подряд.
                # value1 = input - это ввод пользователем ОБЫЧНЫХ чисел.
                value1 = input('Сумма двух чисел подряд (например, 23+12):~$ ')

                # numbers - хранит УЖЕ ПЕРЕВЕДЁННЫЕ в СПЛИТ, то есть в СПИСОК, числа с определённым знаком.
                # .split() - переводит значение, введённое пользователем, в список.
                # Знак " + " в данном случае, будет выступать разделителем между элементами списка. Он не настоящий.
                numbers = value1.split('+')

                # Теперь нужно разбить СПЛИТ на пары. То есть, то что мы будем складывать, вычитать, умножать и делить.
                # Это делается с помощью двух переменных - a и b.
                a = int(numbers[0]) # Переменная а - первое число списка, оно обозначено как 0.
                b = int(numbers[1]) # Переменная b - второе число списка, оно обозначено как 1.

                # Переменная result, в отличие от .split-a уже реально складывает переменные a и b,
                # .split же, просто выводит нам значок плюсика.
                result = a + b

                # Принтуем результат.
                # value1 - это то, что вводит пользователь.
                # result - это непосредственно сам результат, то есть, то, что получилось.
                print('\n',f'Сумма чисел {value1} равна {result}','\n')

                # Кнопка 2. Разница двух чисел.
            elif (value == 2):
                value1 = input('Разница двух чисел подряд (например, 23-12):~$ ')
                numbers = value1.split('-')
                a = int(numbers[0])
                b = int(numbers[1])
                result = a - b
                print('\n',f'Разница чисел {value1} равна {result}','\n')

                # Кнопка 3. Произведение двух чисел.
            elif (value == 3):
                value1 = input('Произведение двух чисел подряд (например, 23*12):~$ ')
                numbers = value1.split('*')
                a = int(numbers[0])
                b = int(numbers[1])
                result = a * b
                print('\n',f'Произведение чисел {value1} равно {result}','\n')

                # Кнопка 4. Частное двух чисел.
            elif (value == 4):
                try:

                    value1 = input('Частное двух чисел подряд (например, 23/12):~$ ')
                    numbers = value1.split('/')
                    a = int(numbers[0])
                    b = int(numbers[1])
                    result = a / b
                    print('\n', f'Частное двух чисел {value1} равно {result}', '\n')

                except ZeroDivisionError:
                    print('\nОшибка! На ноль делить нельзя.\n')
                except (ValueError, IndexError):
                    print('\nНекорректный формат ввода. Попробуйте ещё раз.\n')

                # Кнопка 5. Выход из программы.
            elif (value == 5):
                print('\nВыход из программы.')
                break

            else:
                print('\nНекорректный выбор. Пожалуйста, выберите число от 1 до 5.\n')

    except: continue

# break # выключен, перезапускаем программу...